package jhs_web;

public class User {
	// 멤버변수 선언

	private String us_name;
	private String us_identification;
	private String us_password;
	private String us_password2;
	

	public String getUs_name() {
		return us_name;
	}
	public void setUs_name(String us_name) {
		this.us_name = us_name;
	}
	public String getUs_identification() {
		return us_identification;
	}
	public void setUs_identification(String us_identification) {
		this.us_identification = us_identification;
	}
	public String getUs_password() {
		return us_password;
	}
	public void setUs_password(String us_password) {
		this.us_password = us_password;
	}
	public String getUs_password2() {
		return us_password2;
	}
	public void setUs_password2(String us_password2) {
		this.us_password2 = us_password2;
	}
}
